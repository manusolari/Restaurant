 public void llenarCombo1() {
        jCBMesa.addItem(null);
        ArrayList<Mesa> lista = md.mesasOcupadas();
        for (Mesa m : lista) {
            jCBMesa.addItem(m);
        }
    }
 
 public void llenarCombo2(){
        jCBProducto.addItem(null);
        ArrayList<Producto> lista= prodData.listarProductos();
        for (Producto p: lista){
            jCBProducto.addItem(p);
        }
    }
 
  public void armarCabecera(){
        modelo.addColumn("Numero de Mesa");
        modelo.addColumn("IdProducto");
        modelo.addColumn("Producto");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Precio");
        jTabla.setModel(modelo);
    }
  
   public void limpiarTabla() {
        int f = jTabla.getRowCount() - 1;
        for (; f >= 0; f--) {
            modelo.removeRow(f);
        }
    }


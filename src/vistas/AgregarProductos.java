    private void jCBProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCBProductoActionPerformed
        try{
        Mesa m= (Mesa) jCBMesa.getSelectedItem();
        int cantidad= Integer.parseInt(jTFCantidad.getText());
        Producto p= (Producto)jCBProducto.getSelectedItem();
        int idPedido=pd.pedidoXIdMesa(m);
        //Capturo los datos los pongo en la tabla
        modelo.addRow(new Object[]{idPedido, p.getIdProducto(), p.getNombre(), cantidad, p.getPrecio()});
        //Limpio los campos para seguir agregando productos
        jTFCantidad.setText("");
        jCBMesa.setSelectedItem(null);
        jCBProducto.setSelectedItem(null);
        } catch(NullPointerException nc){
            JOptionPane.showMessageDialog(this, "Debe seleccionar un producto, cantidad  idPedido no pueden ser nulos");
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(this, "Los campos no deben ser nulos");
        }
    }//GEN-LAST:event_jCBProductoActionPerformed

    private void jBAgregarProductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarProductosActionPerformed
        try{
        //Se agregan todos los productos de la tabla
        for(int i=0; i<modelo.getRowCount(); i++){
            Integer idPedido= (Integer)( modelo.getValueAt(i, 0));
            Integer idProducto= (Integer) modelo.getValueAt(i, 1);
            Integer cantidad= (Integer)modelo.getValueAt(i, 3);
            Pedido p= pd.buscarPedidoXid(idPedido);
            
            Producto pr= prodData.buscarProductoPorId(idProducto);
            
            PedidoProducto pedProd= new PedidoProducto(pr, p, cantidad);
            ppd.iniciarPedidoProducto(pedProd);   
        }
        //Limpio la tabla
        limpiarTabla();
        }catch(NullPointerException nc){
            JOptionPane.showMessageDialog(this, "Debe seleccionar un producto");
        }
    }//GEN-LAST:event_jBAgregarProductosActionPerformed

